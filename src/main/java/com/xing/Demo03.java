package com.xing;

public class Demo03 {
    public static void main(String[] args) {
        //Java八大基本数据类型

        //整数
        byte  num1=1;
        short num2=2;
        int   num4=4; //最常用
        long  num8=8L; //long类型要在数字后面加个大写的L

        //小数：浮点数
        float num44= 4.4f; //float类型要在数值后面加上一个f
        double num88=8.8;

        //字符
        char num22='一'; //char类型只能有一个字符，多了不行
        //String是一个类，一个字符串
        String num222="我是类，是字符串，不是基本类型";

        //布尔值,只有两个目标值，true和false
        boolean flag0=true;
        boolean flag1=false;

    }


}
